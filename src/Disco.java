public class Disco {
	private int capacidad;
	private TipoDisco tipo;

	public int getCapacidad() {
		return capacidad;
	}

	public void setCapacidad(int capacidad) {
		this.capacidad = capacidad;
	}

	public TipoDisco getTipo() {
		return tipo;
	}

	public void setTipo(TipoDisco tipo) {
		this.tipo = tipo;
	}
}
